def generate_pdf_summary(best_role, best_match, missing_skills, courses):
    buffer = BytesIO()
    c = canvas.Canvas(buffer, pagesize=letter)
    width, height = letter

    # Title
    y = height - 50
    c.setFont("Helvetica-Bold", 16)
    c.setFillColor(colors.darkblue)
    c.drawString(50, y, "ğŸ“„ Smart Career Recommender - Summary Report")

    # Best Role
    y -= 40
    c.setFont("Helvetica-Bold", 12)
    c.setFillColor(colors.black)
    c.drawString(50, y, "ğŸ¯ Best Matched Role:")
    c.setFont("Helvetica", 12)
    c.drawString(200, y, best_role)

    # Match %
    y -= 20
    c.setFont("Helvetica-Bold", 12)
    c.drawString(50, y, "ğŸ“Š Match Percentage:")
    c.setFont("Helvetica", 12)
    c.drawString(200, y, f"{best_match}%")

    # Missing Skills
    y -= 30
    c.setFont("Helvetica-Bold", 12)
    c.setFillColor(colors.darkred)
    c.drawString(50, y, "âŒ Missing Skills:")
    y -= 20
    c.setFont("Helvetica", 11)
    c.setFillColor(colors.black)
    if missing_skills:
        for skill in missing_skills:
            c.drawString(70, y, f"- {skill}")
            y -= 15
    else:
        c.drawString(70, y, "None")

    # Course Recommendations
    y -= 30
    c.setFont("Helvetica-Bold", 12)
    c.setFillColor(colors.darkgreen)
    c.drawString(50, y, "ğŸ“š Recommended Courses:")
    y -= 20
    c.setFont("Helvetica", 11)
    c.setFillColor(colors.black)
    for skill in missing_skills:
        rec = courses.get(skill.lower())
        if rec:
            course_line = f"- {skill.title()}: {rec['course']} ({rec['platform']})"
            c.drawString(70, y, course_line)
            y -= 15

    # Footer
    y -= 40
    c.setFont("Helvetica-Oblique", 10)
    c.setFillColor(colors.grey)
    c.drawString(50, y, "Generated by Smart Career Recommender | Manojit Karar")

    c.save()
    buffer.seek(0)
    return buffer

